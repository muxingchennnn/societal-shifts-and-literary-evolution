<article>
	<p>
		Genre is an ever-evolving concept! Authors and audiences are influenced by the time & society in
		which they live; as a result, the categories into which we place stories evolves alongside them.
		What was perhaps categorized as “weird fiction” at the time of publication may now be known as
		“horror fiction.” Books which might today be marketed as “dark paranormal romantasy with science
		fiction and horror elements” could have been published as merely “gothic” several decades ago.
	</p>
	<p>
		The technology and societal norms of the day matter just as much in the categorization as they
		do the writing of the book itself. For example, The Martian (2011) versus The War of the Worlds
		(1897).
	</p>
