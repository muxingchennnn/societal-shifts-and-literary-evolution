		Where The Martian imagines realistically what could go wrong with technology does exist, Wells’
		imagination runs wild with.
	</p>
	<p>
		A book like The Martian couldn’t have been written back in the 1890s, and The War of the Worlds
		would be a fundamentally different story if it was written today.
