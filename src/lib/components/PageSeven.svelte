	<p>
		<span class="italic-serif">The Martian </span> (2011): Andy Weir really brought the “science”
		part of “science fiction” to his debut novel. <span class="italic-serif">The Martian </span> is extremely
		technically accurate, chronicling one man’s survival on the planet Mars after being left stranded
		by a mission gone awry. It delves into detailed yet surprisingly engaging explanations of how the
		protagonist Mark Watney creates water by burning hydrogen gas, jerry-rigs antiquated electronics
		to re-establish communication with Earth, and generally engineers his continued survival against
		all odds.
